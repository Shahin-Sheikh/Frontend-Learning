// First order function is a function that does not take another function as an argument
// and does not return a function as its result

// Function that adds two numbers
function add(a, b) {
  return a + b;
}

console.log(add(2, 3)); // Outputs: 5

// Function that multiplies two numbers
function multiply(a, b) {
  return a * b;
}

console.log(multiply(4, 5)); // Outputs: 20

// Function that calculates the square of a number
function square(x) {
  return x * x;
}

console.log(square(6)); // Outputs: 36
